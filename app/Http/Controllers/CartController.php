<?php

namespace App\Http\Controllers;

use App\Models\Product;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Session;

class CartController extends Controller
{
    public function index()
    {
        $cart = $this->getCart();
        $total = $this->getCartTotal();
        $taxRate = config('ecommerce.tax_rate', 0.18);
        return view('cart.index', compact('cart', 'total', 'taxRate'));
    }

    public function add(Request $request, Product $product)
    {
        $request->validate([
            'quantity' => 'required|integer|min:1'
        ]);

        if ($product->stock_quantity < $request->quantity) {
            return redirect()->back()->with('error', 'Yetersiz stok!');
        }

        $cart = $this->getCart();
        $productId = $product->id;

        if (isset($cart[$productId])) {
            $cart[$productId]['quantity'] += $request->quantity;
        } else {
            $cart[$productId] = [
                'id' => $product->id,
                'name' => $product->name,
                'price' => $product->getCurrentPrice(),
                'quantity' => $request->quantity,
                'image' => $product->getMainImage()
            ];
        }

        Session::put('cart', $cart);

        return redirect()->back()->with('success', 'Ürün sepete eklendi!');
    }

    public function update(Request $request, $productId)
    {
        $request->validate([
            'quantity' => 'required|integer|min:1|max:999'
        ]);

        $cart = $this->getCart();

        if (isset($cart[$productId])) {
            // Stok kontrolü
            $product = Product::findOrFail($productId);

            if ($request->quantity > $product->stock_quantity) {
                return redirect()->back()->with('error', 'Yetersiz stok! Maksimum ' . $product->stock_quantity . ' adet sipariş verebilirsiniz.');
            }

            $cart[$productId]['quantity'] = $request->quantity;
            Session::put('cart', $cart);
        }

        return redirect()->route('cart.index')->with('success', 'Sepet güncellendi!');
    }

    public function remove($productId)
    {
        $cart = $this->getCart();

        if (isset($cart[$productId])) {
            unset($cart[$productId]);
            Session::put('cart', $cart);
        }

        return redirect()->route('cart.index')->with('success', 'Ürün sepetten kaldırıldı!');
    }

    private function getCart()
    {
        return Session::get('cart', []);
    }

    public function getCartTotal()
    {
        $cart = $this->getCart();
        $total = 0;

        foreach ($cart as $item) {
            $total += $item['price'] * $item['quantity'];
        }

        return $total;
    }

    public function getCartCount()
    {
        $cart = $this->getCart();
        return array_sum(array_column($cart, 'quantity'));
    }

    public static function getCartCountStatic()
    {
        $cart = Session::get('cart', []);
        return array_sum(array_column($cart, 'quantity'));
    }
}
